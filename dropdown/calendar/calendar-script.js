// ======================================================================

function generateRecurringInstances(baseEvent, recurrence) {
  const instances        = [];
  const baseDate         = new Date(baseEvent.dateKey+'T00:00:00');
  const maxInstances     = recurrence === 'monthly' ? 12 : recurrence === 'biweekly' ? 26 : 52;
  const recurrenceGroupId = `rec_${Date.now()}`;
  for (let i = 0; i < maxInstances; i++) {
    const d = new Date(baseDate);
    if      (recurrence === 'monthly')   d.setMonth(d.getMonth() + i);
    else if (recurrence === 'biweekly') d.setDate(d.getDate() + 14 * i);
    else                                d.setDate(d.getDate() + 7 * i);
    instances.push({ ...baseEvent, id: `${Date.now()}_${i}_${Math.floor(Math.random()*99999)}`, dateKey: getDateKey(d), recurrenceGroupId, recurrence, autoGenerated: i > 0 });
  }
  return instances;
}

