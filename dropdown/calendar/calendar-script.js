function renderWeekView() {
  const el  = document.getElementById('weekViewContent');
  if (!el) return;
  const today    = new Date(); today.setHours(0,0,0,0);
  const base     = new Date(today);
  base.setDate(today.getDate() - today.getDay() + (weekOffset * 7));
  const days     = Array.from({ length: 7 }, (_, i) => { const d = new Date(base); d.setDate(base.getDate()+i); return d; });
  const mons     = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  const dows     = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
  const todayKey = getDateKey(new Date());

  const headerHTML = days.map(d => {
    const isT = getDateKey(d) === todayKey;
    return `<div class="week-col-header${isT ? ' week-today' : ''}">
      <div class="week-dow">${dows[d.getDay()]}</div>
      <div class="week-date-num${isT ? ' week-date-today' : ''}">${d.getDate()}</div>
      <div class="week-month-lbl">${mons[d.getMonth()]}</div>
    </div>`;
  }).join('');

  const eventsHTML = days.map(d => {
    const dk  = getDateKey(d);
    const isT = dk === todayKey;
    const evs = getActiveEvents().filter(e => {
      if (e.dateKey !== dk) return false;
      if (calendarMode === 'community') return true;
      if (e.type === 'task' || e.type === 'harvest' || e.type === 'pest') return activeLayers.tasks;
      if (e.type === 'da')     return activeLayers.da;
      if (e.type === 'market') return activeLayers.market;
      return true;
    });
    return `<div class="week-col-events${isT ? ' week-today-col' : ''}">
      ${evs.map(e => `<div class="week-event-pill type-${e.type}" onclick="openDayDetail('${dk}')" title="${(e.notes||'').replace(/"/g,'&quot;')}">${e.emoji} <span>${e.title}</span></div>`).join('')}
      ${calendarMode === 'personal' ? `<button class="week-add-btn" onclick="openAddEventModalForDay('${dk}')" title="Add event">+</button>` : ''}
    </div>`;
  }).join('');

  const rangeLabel = days[0].getMonth() === days[6].getMonth()
    ? `${mons[days[0].getMonth()]} ${days[0].getDate()}–${days[6].getDate()}, ${days[0].getFullYear()}`
    : `${mons[days[0].getMonth()]} ${days[0].getDate()} – ${mons[days[6].getMonth()]} ${days[6].getDate()}, ${days[6].getFullYear()}`;

  el.innerHTML = `
    <div class="week-view-wrap">
      <div class="week-nav">
        <button onclick="prevWeek()" class="nav-arrow"><i class="fas fa-chevron-left"></i></button>
        <span class="week-range-label">${rangeLabel}</span>
        <button onclick="nextWeek()" class="nav-arrow"><i class="fas fa-chevron-right"></i></button>
        <button onclick="weekOffset=0;renderWeekView();" class="nav-today-btn">This Week</button>
      </div>
      <div class="week-grid">
        <div class="week-header-row">${headerHTML}</div>
        <div class="week-events-row">${eventsHTML}</div>
      </div>
    </div>`;
}

function prevWeek() { weekOffset--; renderWeekView(); }
function nextWeek() { weekOffset++; renderWeekView(); }

