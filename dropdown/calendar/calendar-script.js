function undoLastAction() {
  if (!historyStack.length) { showToast('Nothing to undo'); return; }
  const last = historyStack.pop(); redoStack.push(last);
  if (last.action === 'addEvents') {
    const ids = new Set(last.events.map(e => String(e.id)));
    calendarEvents = calendarEvents.filter(e => !ids.has(String(e.id)));
    saveToLocalStorage(); renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
    showToast(`↩️ ${last.events.length} event(s) removed`);
  } else if (last.action === 'editEvent') {
    const idx = calendarEvents.findIndex(e => String(e.id) === last.eventId);
    if (idx !== -1) calendarEvents[idx] = last.before;
    saveToLocalStorage(); renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
    showToast('↩️ Edit undone');
  }
}

function redoLastAction() {
  if (!redoStack.length) { showToast('Nothing to redo'); return; }
  const last = redoStack.pop(); historyStack.push(last);
  if (last.action === 'addEvents') {
    calendarEvents.push(...last.events);
    saveToLocalStorage(); renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
    showToast(`↪️ ${last.events.length} event(s) restored`);
  } else if (last.action === 'editEvent') {
    const idx = calendarEvents.findIndex(e => String(e.id) === last.eventId);
    if (idx !== -1) calendarEvents[idx] = last.after;
    saveToLocalStorage(); renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
    showToast('↪️ Edit redone');
  }
}

