function renderMiniCalendar() {
  const grid  = document.getElementById('miniCalGrid');
  const label = document.getElementById('miniCalMonthLabel');
  if (!grid) return;
  const mons = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  if (label) label.textContent = `${mons[currentMonth]} ${currentYear}`;
  const firstDay    = new Date(currentYear, currentMonth, 1).getDay();
  const daysInMonth = new Date(currentYear, currentMonth+1, 0).getDate();
  const todayKey    = getDateKey(new Date());
  const events      = getActiveEvents();
  let html = '';
  for (let i = 0; i < firstDay; i++) html += '<div class="mini-cal-day"></div>';
  for (let d = 1; d <= daysInMonth; d++) {
    const dk       = `${currentYear}-${String(currentMonth+1).padStart(2,'0')}-${String(d).padStart(2,'0')}`;
    const isToday  = dk === todayKey;
    const hasEvent = events.some(e => e.dateKey === dk);
    html += `<div class="mini-cal-day${isToday?' mini-today':''}${hasEvent?' mini-has-event':''}">${d}</div>`;
  }
  // Fill to 42 cells so height is always stable (6 rows × 7 cols)
  const filled = firstDay + daysInMonth;
  for (let i = filled; i < 42; i++) html += '<div class="mini-cal-day"></div>';
  grid.innerHTML = html;
}

