function initRecurringMarketDays() {
  calendarEvents = calendarEvents.filter(e => typeof e.id === 'number');
  const today   = new Date(); today.setHours(0,0,0,0);
  const endDate = addDays(today, 365);

  // Bi-weekly Saturday market
  let sat = new Date(today);
  while (sat.getDay() !== 6) sat = addDays(sat, 1);
  while (sat <= endDate) {
    calendarEvents.push({ id:`market_${getDateKey(sat)}`, dateKey:getDateKey(sat), type:'market', formType:'marketDay', emoji:'🛒', title:'Oroquieta Public Market Day', notes:'Best prices for livestock & produce.', location:'Oroquieta City Market', recurrent:true, autoGenerated:true });
    sat = addDays(sat, 14);
  }

  // Monthly Sunday livestock trading
  let sun = new Date(today);
  while (sun.getDay() !== 0) sun = addDays(sun, 1);
  while (sun <= endDate) {
    calendarEvents.push({ id:`trade_${getDateKey(sun)}`, dateKey:getDateKey(sun), type:'market', formType:'marketDay', emoji:'🐄', title:'Livestock Trading Post', notes:'Monthly livestock barter.', location:'Oroquieta Trading Post', recurrent:true, autoGenerated:true });
    sun = addDays(sun, 28);
  }
}

