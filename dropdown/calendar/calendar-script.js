async function fetchWeatherFromPAGASA() {
  try {
    const url = 'https://api.open-meteo.com/v1/forecast?latitude=8.4853&longitude=123.7886' +
      '&current=temperature_2m,relative_humidity_2m,wind_speed_10m,weather_code' +
      '&daily=weather_code,temperature_2m_max,temperature_2m_min,precipitation_probability_max' +
      '&timezone=Asia%2FManila&forecast_days=7';
    const res  = await fetch(url);
    const data = await res.json();
    const wmoIcon = c => c===0?'☀️':c<=3?'⛅':c<=48?'🌫️':c<=67?'🌧️':c<=77?'❄️':c<=82?'🌦️':'⛈️';
    const wmoDesc = c => c===0?'Clear Sky':c<=3?'Partly Cloudy':c<=48?'Foggy':c<=67?'Rainy':c<=82?'Showers':'Thunderstorm';
    const days = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
    const forecast = data.daily.time.slice(0,7).map((d,i) => ({
      day:     i===0 ? 'Today' : days[new Date(d+'T00:00:00').getDay()],
      dateKey: d,
      icon:    wmoIcon(data.daily.weather_code[i]),
      high:    Math.round(data.daily.temperature_2m_max[i]),
      low:     Math.round(data.daily.temperature_2m_min[i]),
      rain:    `${data.daily.precipitation_probability_max[i]}%`
    }));
    return {
      live: true,
      location: 'Oroquieta City, Misamis Occidental',
      current: {
        temp:      Math.round(data.current.temperature_2m),
        humidity:  data.current.relative_humidity_2m,
        condition: wmoDesc(data.current.weather_code),
        icon:      wmoIcon(data.current.weather_code),
        wind:      `${Math.round(data.current.wind_speed_10m)} km/h`
      },
      forecast, alert: null
    };
  } catch {
    const today = new Date();
    WEATHER_PLACEHOLDER.forecast = WEATHER_PLACEHOLDER.forecast.map((f,i) => ({
      ...f, dateKey: getDateKey(addDays(today,i))
    }));
    return WEATHER_PLACEHOLDER;
  }
}

