function renderLifecycleProgress() {
  const el = document.getElementById('lifecycleProgress');
  if (!el) return;
  const todayKey  = getDateKey(new Date());
  const lifecycles = {};
  calendarEvents.forEach(e => {
    if (!e.lifecycleId) return;
    if (!lifecycles[e.lifecycleId]) lifecycles[e.lifecycleId] = { crop: e.crop, events: [] };
    lifecycles[e.lifecycleId].events.push(e);
  });
  const active = Object.values(lifecycles).filter(lc => {
    const dates = lc.events.map(e => e.dateKey).sort();
    return dates[0] <= todayKey && dates[dates.length-1] >= todayKey;
  });
  if (!active.length) {
    el.innerHTML = '<p style="color:#888;font-size:12px;padding:4px 0">No active crop lifecycles. Plant a crop to start tracking!</p>';
    return;
  }
  const cropEmojis = { rice:'🌾', corn:'🌽', vegetables:'🥬', banana:'🍌', coconut:'🥥', cassava:'🍠', sweetPotato:'🍠' };
  el.innerHTML = active.map(lc => {
    const sorted   = [...lc.events].sort((a,b) => a.dateKey.localeCompare(b.dateKey));
    const start    = sorted[0].dateKey;
    const end      = sorted[sorted.length-1].dateKey;
    const total    = Math.max(1, Math.round((new Date(end+'T00:00:00') - new Date(start+'T00:00:00')) / 86400000));
    const elapsed  = Math.round((new Date(todayKey+'T00:00:00') - new Date(start+'T00:00:00')) / 86400000);
    const pct      = Math.min(100, Math.max(0, Math.round((elapsed / total) * 100)));
    const done     = sorted.filter(e => e.dateKey <= todayKey);
    const next     = sorted.find(e => e.dateKey > todayKey);
    const emoji    = cropEmojis[lc.crop] || '🌱';
    const daysLeft = getDaysUntil(end);
    return `<div class="lifecycle-progress-card">
      <div class="lc-header">
        <span class="lc-crop">${emoji} ${lc.crop ? lc.crop.charAt(0).toUpperCase() + lc.crop.slice(1) : 'Crop'}</span>
        <span class="lc-pct">${pct}%</span>
      </div>
      <div class="lc-bar-bg"><div class="lc-bar-fill" style="width:${pct}%"></div></div>
      <div class="lc-meta">Day ${elapsed} of ${total} • ${daysLeft > 0 ? daysLeft + 'd remaining' : '🏁 Final stage'}</div>
      ${done.length ? `<div class="lc-last">✅ ${done[done.length-1].title}</div>` : ''}
      ${next ? `<div class="lc-next">⏭️ ${next.title} in ${getDaysUntil(next.dateKey)}d</div>` : '<div class="lc-next">🎉 Harvest is here!</div>'}
    </div>`;
  }).join('');
}

