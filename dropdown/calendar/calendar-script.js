function getDateKey(date) {
  return `${date.getFullYear()}-${String(date.getMonth()+1).padStart(2,'0')}-${String(date.getDate()).padStart(2,'0')}`;
}
function addDays(date, n) { const d = new Date(date); d.setDate(d.getDate()+n); return d; }
function formatDateLabel(dateKey) {
  const d    = new Date(dateKey+'T00:00:00');
  const days = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
  const mons = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  return `${days[d.getDay()]}, ${mons[d.getMonth()]} ${d.getDate()}`;
}
function getDaysUntil(dateKey) {
  const today  = new Date(); today.setHours(0,0,0,0);
  const target = new Date(dateKey+'T00:00:00');
  return Math.round((target - today) / 86400000);
}

const MOON_PHASES = ['🌑','🌒','🌓','🌔','🌕','🌖','🌗','🌘'];
const MOON_NAMES  = ['New Moon','Waxing Crescent','First Quarter','Waxing Gibbous','Full Moon','Waning Gibbous','Last Quarter','Waning Crescent'];
function getMoonPhase(date) {
  const days = Math.floor((date - new Date(2000,0,6)) / 86400000) % 29.53;
  return MOON_PHASES[Math.abs(Math.floor((days/29.53)*8) % 8)];
}
function getMoonPhaseName(date) {
  const days = Math.floor((date - new Date(2000,0,6)) / 86400000) % 29.53;
  return MOON_NAMES[Math.abs(Math.floor((days/29.53)*8) % 8)];
}

