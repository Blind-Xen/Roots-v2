function renderCalendar() {
  const grid       = document.getElementById('calendarGrid');
  const monthNames = ['January','February','March','April','May','June','July','August','September','October','November','December'];
  document.getElementById('monthName').textContent = monthNames[currentMonth];
  document.getElementById('yearName').textContent  = currentYear;

  const today       = new Date();
  const firstDay    = new Date(currentYear, currentMonth, 1).getDay();
  const daysInMonth = new Date(currentYear, currentMonth+1, 0).getDate();
  const daysInPrev  = new Date(currentYear, currentMonth, 0).getDate();
  const events      = getActiveEvents();

  // Build lifecycle stripe date ranges
  const lifecycleMap = {};
  events.forEach(e => {
    if (!e.lifecycleId) return;
    if (!lifecycleMap[e.lifecycleId]) lifecycleMap[e.lifecycleId] = { min: e.dateKey, max: e.dateKey };
    if (e.dateKey < lifecycleMap[e.lifecycleId].min) lifecycleMap[e.lifecycleId].min = e.dateKey;
    if (e.dateKey > lifecycleMap[e.lifecycleId].max) lifecycleMap[e.lifecycleId].max = e.dateKey;
  });

  let html = '';

  for (let i = 0; i < firstDay; i++) {
    html += `<div class="cal-day other-month"><div class="cal-date">${daysInPrev - firstDay + 1 + i}</div></div>`;
  }

  for (let day = 1; day <= daysInMonth; day++) {
    const cellDate  = new Date(currentYear, currentMonth, day);
    const dateKey   = getDateKey(cellDate);
    const isToday   = day === today.getDate() && currentMonth === today.getMonth() && currentYear === today.getFullYear();
    const isWeekend = cellDate.getDay() === 0 || cellDate.getDay() === 6;

    const dayEvents = events.filter(e => {
      if (e.dateKey !== dateKey) return false;
      if (calendarMode === 'community') return true;
      if (e.type === 'task' || e.type === 'harvest' || e.type === 'pest') return activeLayers.tasks;
      if (e.type === 'da')     return activeLayers.da;
      if (e.type === 'market') return activeLayers.market;
      return false;
    });

    const dots = dayEvents.slice(0, 4).map(e => {
      const cls = e.type==='da' ? 'dot-da' : e.type==='market' ? 'dot-market' : e.type==='harvest' ? 'dot-harvest' : e.type==='pest' ? 'dot-pest' : 'dot-task';
      return `<div class="cal-dot ${cls}" title="${e.title}"></div>`;
    }).join('') + (dayEvents.length > 4 ? `<div class="cal-dot-more">+${dayEvents.length - 4}</div>` : '');

    let label = '';
    if (dayEvents.length > 0) {
      const f  = dayEvents[0];
      const lc = f.type==='da' ? 'label-da' : f.type==='market' ? 'label-market' : f.type==='harvest' ? 'label-harvest' : f.type==='pest' ? 'label-pest' : 'label-task';
      label = `<div class="cal-event-label ${lc}">${f.emoji} ${f.title}</div>`;
    }

    const moon = activeLayers.moon
      ? `<div class="cal-moon" title="${getMoonPhaseName(cellDate)}">${getMoonPhase(cellDate)}</div>` : '';

    let weatherDot = '';
    if (activeLayers.weather) {
      const rain = weatherCache?.dailyRain?.[dateKey];
      if (rain !== undefined && rain >= 50) {
        weatherDot = `<div class="cal-dot dot-weather" title="Rain: ${rain}%" style="background:${rain>=80?'#e74c3c':'#3498db'}"></div>`;
      }
    }

    const stripe = calendarMode === 'personal' && Object.values(lifecycleMap).some(lc => dateKey >= lc.min && dateKey <= lc.max)
      ? '<div class="lifecycle-stripe"></div>' : '';

    const farmerBadge = calendarMode === 'community' && dayEvents.length > 0
      ? `<div class="community-farmer-badge">${dayEvents[0].farmer || ''}</div>` : '';

    const density = dayEvents.length === 0 ? '' : dayEvents.length <= 2 ? 'heat-low' : dayEvents.length <= 4 ? 'heat-mid' : 'heat-high';

    html += `
      <div class="cal-day ${isToday?'today':''} ${isWeekend?'weekend':''} ${density}"
           onclick="handleDayClick('${dateKey}')" title="${formatDateLabel(dateKey)}">
        <div class="cal-date">${day}</div>
        ${stripe}
        <div class="cal-dots">${dots}${weatherDot}</div>
        ${label}${moon}${farmerBadge}
      </div>`;
  }

  const trailing = 42 - (firstDay + daysInMonth);
  for (let i = 1; i <= trailing; i++) html += `<div class="cal-day other-month"><div class="cal-date">${i}</div></div>`;

  grid.innerHTML = html;
}

