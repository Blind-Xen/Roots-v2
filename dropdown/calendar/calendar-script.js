function renderStats() {
  const strip  = document.getElementById('statsStrip');
  const current = weatherCache?.data?.current;
  const today = getDateKey(new Date());
  const forecastToday = weatherCache?.data?.forecast?.find(f => f.dateKey === today);
  if (!current && !forecastToday) {
    strip.innerHTML = `<div class="weather-card"><div class="wday">No weather data</div><div class="wdesc">Waiting for forecast...</div></div>`;
    return;
  }
  const icon = current?.icon || forecastToday?.icon || '☀️';
  const label = current?.condition || (forecastToday ? 'Forecast' : 'Weather');
  const temp = current ? `${current.temp}°C` : `${forecastToday.high}° / ${forecastToday.low}°`;
  const detail = current ? `💧 ${current.humidity}% • 🌬️ ${current.wind}` : forecastToday ? `🌧 ${forecastToday.rain}` : '';
  strip.innerHTML = `
    <div class="weather-card">
      <div class="wday">Today</div>
      <div class="wicon">${icon}</div>
      <div class="wtemps">${temp}</div>
      <div class="wdesc">${label}</div>
      <div class="wrain">${detail}</div>
    </div>`;
}

