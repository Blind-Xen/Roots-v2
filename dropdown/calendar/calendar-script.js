function submitEvent() {
  const modal      = document.getElementById('addEventModal');
  const editingId  = modal.dataset.editingId || null;
  const type       = document.getElementById('eventType').value;
  const title      = document.getElementById('eventTitle').value.trim();
  const dateKey    = document.getElementById('eventDate').value;
  const time       = document.getElementById('eventTime').value;
  const location   = document.getElementById('eventLocation').value.trim();
  const notes      = document.getElementById('eventNotes').value.trim();
  const crop       = document.getElementById('eventCrop')?.value || '';
  const isPublic   = document.getElementById('eventIsPublic')?.checked || false;
  const recurrence = document.getElementById('eventRecurrence')?.value || 'none';

  if (!type)    { showToast('⚠️ Please select an event type'); showFieldError('eventType'); return; }
  if (!title)   { showToast('⚠️ Please enter an event title'); showFieldError('eventTitle'); return; }
  if (!dateKey) { showToast('⚠️ Please select a date');        showFieldError('eventDate'); return; }

  if (editingId !== null) {
    const idx = calendarEvents.findIndex(e => String(e.id) === String(editingId));
    if (idx !== -1) {
      const ex      = calendarEvents[idx];
      const updated = { ...ex, type: mapFormTypeToInternal(type), formType: type, emoji: getEmojiForType(type), title: title + (time ? ` @ ${time}` : ''), notes: notes || ex.notes, location: location || ex.location, crop: crop || ex.crop, dateKey, autoGenerated: ex.autoGenerated };
      historyStack.push({ action: 'editEvent', eventId: String(ex.id), before: { ...ex }, after: updated }); redoStack = [];
      calendarEvents[idx] = updated;
      saveToLocalStorage();
      showToast('✅ Event updated! (Ctrl+Z to undo)');
    }
    resetAndClose(); return;
  }

  const newEvent = { id: String(Date.now()), dateKey, type: mapFormTypeToInternal(type), formType: type, emoji: getEmojiForType(type), title: title + (time ? ` @ ${time}` : ''), notes: notes || undefined, location: location || undefined, crop: crop || undefined, autoGenerated: false, isPublic };
  let added = [newEvent];

  if (type === 'planting' && crop && cropSchedules[crop]) {
    const lc = generateCropLifecycle(dateKey, crop, title);
    if (isPublic) lc.forEach(e => { e.isPublic = true; e.farmer = 'Kuya Mario S.'; });
    added = lc;
    showToast(`✅ Planting day added! ${lc.length} tasks auto-scheduled.`);
  } else if (recurrence !== 'none') {
    added = generateRecurringInstances(newEvent, recurrence);
    showToast(`✅ ${added.length} recurring events added!`);
  } else {
    showToast(isPublic ? '✅ Event added & posted to Community!' : '✅ Event added!');
  }

  historyStack.push({ action: 'addEvents', events: added }); redoStack = [];
  calendarEvents.push(...added);
  saveToLocalStorage();

  if (isPublic) {
    added.forEach(e => {
      if (!communityEvents.some(c => c.id === `pub_${e.id}`))
        communityEvents.unshift({ ...e, id: `pub_${e.id}`, farmer: 'Kuya Mario S.' });
    });
    showToast('📢 Posted to Community Calendar!');
  }
  resetAndClose();
}

