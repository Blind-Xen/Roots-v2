function deleteEvent(eventId) {
  const event = calendarEvents.find(e => String(e.id) === String(eventId));
  if (!event) return;
  if (event.lifecycleId) {
    const grp = calendarEvents.filter(e => e.lifecycleId === event.lifecycleId);
    if (grp.length > 1 && confirm(`This event belongs to a ${event.crop || 'crop'} lifecycle (${grp.length} tasks).\n\nOK = delete all ${grp.length} events\nCancel = delete only this one`)) {
      deleteLifecycle(event.lifecycleId); return;
    }
  }
  historyStack.push({ action: 'addEvents', events: [event] }); redoStack = [];
  calendarEvents  = calendarEvents.filter(e => String(e.id) !== String(eventId));
  communityEvents = communityEvents.filter(e => String(e.id) !== `pub_${eventId}`);
  saveToLocalStorage();
  closeDayDetailModal();
  renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
  showToast('🗑️ Event deleted (Ctrl+Z to undo)');
}

function deleteLifecycle(lifecycleId) {
  const toDelete = calendarEvents.filter(e => e.lifecycleId === lifecycleId);
  if (!toDelete.length) return;
  historyStack.push({ action: 'addEvents', events: toDelete }); redoStack = [];
  calendarEvents = calendarEvents.filter(e => e.lifecycleId !== lifecycleId);
  toDelete.forEach(e => { communityEvents = communityEvents.filter(c => String(c.id) !== `pub_${e.id}`); });
  saveToLocalStorage();
  closeDayDetailModal();
  renderCalendar(); renderStats(); renderUpcoming(); renderDaEvents(); renderLifecycleProgress();
  showToast(`🗑️ ${toDelete.length} lifecycle events deleted (Ctrl+Z to undo)`);
}

