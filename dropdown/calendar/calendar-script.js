function generateCropLifecycle(plantingDateKey, cropType, baseTitle) {
  const schedule = cropSchedules[cropType];
  if (!schedule) return [];
  const plantDate   = new Date(plantingDateKey+'T00:00:00');
  const lifecycleId = `lc_${Date.now()}_${cropType}`;
  return schedule.map((step, i) => ({
    id:            `${Date.now()}_${i}_${Math.floor(Math.random()*99999)}`,
    dateKey:       getDateKey(addDays(plantDate, step.offset)),
    type:          step.type,
    formType:      step.type==='harvest' ? 'harvest' : step.type==='pest' ? 'pestControl' : 'planting',
    emoji:         step.emoji,
    title:         step.title,
    notes:         step.notes || `Auto-generated from: "${baseTitle}"`,
    crop:          cropType,
    autoGenerated: true,
    lifecycleId,
    sourceTitle:   baseTitle
  }));
}

